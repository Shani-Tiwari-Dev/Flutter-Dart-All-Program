void main() {
  int day = 3;
  switch(day) {
    case 1:
      print("Mon");
      break;
    case 2:
      print("Tue");
      break;
    case 3:
      print("Wed");
      break;
    case 4:
      print("Thu");
      break;
    case 5:
      print("Fri");
      break;
    case 6:
      print("Sat");
      break;
    case 7:
      print("Sun");
      break;
    default:
      print("Invalid");
      break;
  }
}
